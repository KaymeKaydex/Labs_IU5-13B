#include <iostream>
#include <string>

using namespace std;
void print(string str)
{
	cout << str;
}
void println(string str)
{
	cout << str << endl;
}
void print(int num)
{
	cout << num;
}
void println(int num)
{
	cout << num << endl;
}
void print(unsigned char buff)
{
	cout << buff;
}
void println(unsigned char buff)
{
	cout << buff << endl;
}
